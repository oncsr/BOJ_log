#include <bits/stdc++.h>
using namespace std;
 
int main(){
    cin.tie(0)->sync_with_stdio(0);
    
    int dx[4] = {-1,0,1,0};
    int dy[4] = {0,1,0,-1};
    
    bool no[10][10][4]{};
    
    no[0][1][0] = no[0][4][0] = no[0][7][0] = 1;
    no[2][1][0] = no[2][4][0] = no[2][7][0] = 1;
    no[3][1][0] = no[3][4][0] = no[3][7][0] = 1;
    no[5][1][0] = no[5][4][0] = no[5][7][0] = 1;
    no[6][1][0] = no[6][4][0] = no[6][7][0] = 1;
    no[8][1][0] = no[8][4][0] = no[8][7][0] = 1;
    no[9][1][0] = no[9][4][0] = no[9][7][0] = 1;
    for(int i=0;i<10;i++) no[1][i][0] = no[4][i][0] = no[7][i][0] = 1;
    
    no[0][1][1] = no[0][3][1] = no[0][7][1] = 1;
    no[1][1][1] = no[1][3][1] = no[1][7][1] = 1;
    no[3][1][1] = no[3][3][1] = no[3][7][1] = 1;
    no[4][1][1] = no[4][3][1] = no[4][7][1] = 1;
    no[7][1][1] = no[7][3][1] = no[7][7][1] = 1;
    no[8][1][1] = no[8][3][1] = no[8][7][1] = 1;
    no[9][1][1] = no[9][3][1] = no[9][7][1] = 1;
    no[2][1][1] = no[2][2][1] = no[2][3][1] = no[2][7][1] = 1;
    no[5][1][1] = no[5][3][1] = no[5][4][1] = no[5][5][1] = no[5][7][1] = no[5][9][1] = 1;
    no[6][1][1] = no[6][3][1] = no[6][4][1] = no[6][5][1] = no[6][7][1] = no[6][9][1] = 1;
    
    no[0][1][2] = no[0][4][2] = 1;
    no[2][1][2] = no[2][4][2] = 1;
    no[3][1][2] = no[3][4][2] = 1;
    no[5][1][2] = no[5][4][2] = 1;
    no[6][1][2] = no[6][4][2] = 1;
    no[8][1][2] = no[8][4][2] = 1;
    no[9][1][2] = no[9][4][2] = 1;
    for(int i=0;i<10;i++) no[1][i][2] = no[4][i][2] = no[7][i][2] = 1;
    
    no[2][2][3] = 1;
    no[4][5][3] = no[4][6][3] = 1;
    no[5][5][3] = no[5][6][3] = 1;
    no[9][5][3] = no[9][6][3] = 1;
    for(int i=0;i<10;i++) no[1][i][3] = no[3][i][3] = no[7][i][3] = 1;
    
    int N, M;
    cin>>N>>M;
    
    char A[1000][1000]{};
    for(int i=0;i<N;i++) for(int j=0;j<M;j++) cin>>A[i][j];
    
    queue<pair<int,int>> Q;
    Q.emplace(0,0);
    
    bool V[1000][1000]{};
    V[0][0]=1;
    
    int cnt = 0;
    while(!Q.empty()){
        auto [x,y] = Q.front();
        Q.pop();
        cnt++;
        
        for(int i=0;i<4;i++){
            int xx = x+dx[i], yy = y+dy[i];
            if(xx<0 || xx>=N || yy<0 || yy>=M || V[xx][yy]) continue;
            if(no[A[x][y]-'0'][A[xx][yy]-'0'][i]) continue;
            V[xx][yy]=1;
            Q.emplace(xx,yy);
        }
    }
    
    cout<<(cnt == N*M ? "YES" : "NO");
    
}
